//Operaciones aritmeticas (suma, resta, multipplicacion, division)
package estructuraSecuencial;
public class ES_Ejercicio1 {
    public static void main(String[] args){
        System.out.println("*****************Ejemplo: Estructura Secuencial**************");
        System.out.println("_____Operacion Suma_____");
        //variables locales
        float a=10; //declarar variable e inicializarla con el valor a 10
        float b= 5;
        float c= 0;
        c=a+b; //suma de a + b
        System.out.println("El resultado de la  suma de: "+a+" + "+b+" es: "+c);
        //operacion resta
        System.out.println("_____Operacion Resta_____");
        float d=20; //declarar variable e inicializarla con el valor a 10
        float f= 15;
        float g= 0;
        g=d-f; 
        System.out.println("El resultado de la resta de: "+d+" - "+f+" es: "+g);
        
        System.out.println("_____Operacion Multiplicacion_____");
        float h=25; //declarar variable e inicializarla con el valor a 10
        float i= 2;
        float j= 0;
        j=h*i; //suma de a + b
        System.out.println("El resultado de la multipplicacion de: "+h+" * "+i+" es: "+j);
        
        System.out.println("_____Operacion Division_____");
        float k=50; //declarar variable e inicializarla con el valor a 10
        float l= 2;
        float m= 0;
        m=k/l; //suma de a + b
        System.out.println("El resultado de la division de: "+k+" / "+l+" es: "+m);
        
        //en este espacio se codifica
    }//Fin del metodo main()
    
}//fin de la clase ES_Ejercicio1