package ciclosForWhileDoWhilw;

public class E1_CicloWhile {
    public static void main(String[] args) {
        System.out.println("Ejemplo del ciclo While");
        int i =1;
        while(i<=10){
            System.out.println(i);
            i++;
        }//Fin ciclo while
    }//fin metodo pricipal
}//fin de la clase
